"use client";

import { Table } from "@tanstack/react-table";
import { X } from "lucide-react";

import { Button } from "@/components/ui/button";
import { Input } from "@/components/ui/input";
import { ReceiptTableViewOptions } from "./ReceiptTableViewOptions";
import { ReceiptTableFacetedFilter } from "./ReceiptTableFacetedFilter";

interface DataTableToolbarProps<TData> {
  table: Table<TData>;
}

export function ReceiptTableToolbar<TData>({
  table,
}: DataTableToolbarProps<TData>) {
  const isFiltered = table.getState().columnFilters.length > 0;

  return (
    <div className="flex items-center justify-between">
      <div className="flex flex-1 items-center space-x-2">
        <Input
          placeholder="Filter receipts..."
          value={(table.getColumn("title")?.getFilterValue() as string) ?? ""}
          onChange={(event) =>
            table.getColumn("title")?.setFilterValue(event.target.value)
          }
          className="h-8 w-[150px] lg:w-[250px]"
        />
        {table.getColumn("status") && (
          <ReceiptTableFacetedFilter
            column={table.getColumn("status")}
            title="Status"
          />
        )}
        {/* {table.getColumn("priority") && (
          <ReceiptTableFacetedFilter
            column={table.getColumn("priority")}
            title="Priority"
            options={priorities}
          />
        )} */}
        {isFiltered && (
          <Button
            variant="ghost"
            onClick={() => table.resetColumnFilters()}
            className="h-8 px-2 lg:px-3"
          >
            Reset
            <X />
          </Button>
        )}
      </div>
      <ReceiptTableViewOptions table={table} />
    </div>
  );
}
